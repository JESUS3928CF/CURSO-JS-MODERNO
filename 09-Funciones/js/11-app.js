/// Esta función recibe dos parámetros:
//* 1: Lo que va a hacer 
//* 2: cada cuanto lo va a hacer en mili segundos 
setInterval( () => console.log("Cada segundo") , 1000);



/// Esta función recibe dos parámetros:
//* 1: Lo que va a hacer 
//* 2: después de cuanto lo va a hacer en mili segundos 
setTimeout( () => console.log("Después de un segundo") , 1000);


